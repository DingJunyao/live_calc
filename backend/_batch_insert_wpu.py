try:
    for entity_id, unit_name, weight_per_unit in INGREDIENT_DATA:
        try:
            # Check if already exists (unique constraint)
            existing = db.query(EntityUnitOverride).filter_by(
                entity_type='ingredient',
                entity_id=entity_id,
                unit_name=unit_name
            ).first()
            if existing:
                skipped += 1
                continue
            obj = EntityUnitOverride(
                entity_type='ingredient',
                entity_id=entity_id,
                unit_name=unit_name,
                weight_per_unit=weight_per_unit,
                weight_unit_id=3,  # g
                source='agent',
                is_default=False
            )
            db.add(obj)
            db.flush()
            success += 1
        except Exception as e:
            errors += 1
            print(f'ERROR at ({entity_id}, {unit_name}): {e}')
    db.commit()
    total = db.query(EntityUnitOverride).count()
    print(f'TOTAL rows in table: {total}')
    print(f'Newly inserted: {success}')
    print(f'Already existed (skipped): {skipped}')
    print(f'Errors: {errors}')
    print(f'TOTAL data entries: {len(INGREDIENT_DATA)}')
except Exception as e:
    db.rollback()
